DECLARE
    NEWCOMM EMP.COMM%TYPE;
BEGIN
    SELECT COMM INTO NEWCOMM FROM EMP WHERE EMPNO=7369;
    IF NEWCOMM IS NULL THEN
        UPDATE EMP SET COMM=3500 WHERE EMPNO=7369;
    ELSE
        NEWCOMM:=NEWCOMM*1.25;
        UPDATE EMP SET COMM=NEWCOMM WHERE EMPNO=7369;
    END IF;
END;
