DECLARE 
    DA NUMBER(4);
    SA EMP.SAL%TYPE;
    EMP_NO EMP.EMPNO%TYPE;
BEGIN
    EMP_NO:=&EMP_NO;
    SELECT MONTHS_BETWEEN(SYSDATE,HIREDATE) INTO DA FROM EMP WHERE EMPNO=EMP_NO;
